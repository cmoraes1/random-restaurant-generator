import UIKit

extension UIColor {
    
    static var newPink: UIColor {
        return UIColor(red: 221/255, green: 88/255, blue: 88/255, alpha: 1.0)
    }
    
    static var newGray: UIColor {
        return UIColor(red: 197/255, green: 197/255, blue: 197/255, alpha: 1)
    }
    
}
